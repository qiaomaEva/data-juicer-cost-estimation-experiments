import argparse
from pathlib import Path

import numpy as np
import pandas as pd

from project_paths import REPORTS_DIR, SUMMARIES_DIR, ensure_output_dirs


TRUE_COL = "op_process_time"
PRED_COL = "op_process_time_pred"


def infer_suffix(pred_path):
    stem = Path(pred_path).stem
    prefix = "chunk_runtime_rows"
    if stem.startswith(prefix):
        return stem[len(prefix) :]
    return f"_{stem}"


def format_number(value, digits=2):
    if pd.isna(value):
        return "nan"
    return f"{float(value):.{digits}f}"


def metric_block(df):
    y_true_raw = pd.to_numeric(df[TRUE_COL], errors="coerce")
    y_pred_raw = pd.to_numeric(df[PRED_COL], errors="coerce")
    valid_mask = (
        y_true_raw.notna()
        & y_pred_raw.notna()
        & np.isfinite(y_true_raw)
        & np.isfinite(y_pred_raw)
        & (y_true_raw > 0)
    )

    y_true = y_true_raw[valid_mask]
    y_pred = y_pred_raw[valid_mask]
    if len(y_true) == 0:
        return {
            "row_count": int(len(df)),
            "sample_count": 0,
            "skipped_count": int(len(df)),
            "rmse": np.nan,
            "mae": np.nan,
            "mape": np.nan,
            "accuracy": np.nan,
            "actual_time_sum": np.nan,
            "pred_time_sum": np.nan,
        }

    abs_error = (y_true - y_pred).abs()
    ape = abs_error / y_true * 100
    return {
        "row_count": int(len(df)),
        "sample_count": int(len(y_true)),
        "skipped_count": int(len(df) - len(y_true)),
        "rmse": float(np.sqrt(np.mean((y_true - y_pred) ** 2))),
        "mae": float(abs_error.mean()),
        "mape": float(ape.mean()),
        "accuracy": float(100 - ape.mean()),
        "actual_time_sum": float(y_true.sum()),
        "pred_time_sum": float(y_pred.sum()),
    }


def summarize_group(df, level, group_cols):
    rows = []
    if not group_cols:
        metrics = metric_block(df)
        metrics["level"] = level
        rows.append(metrics)
        return rows

    for keys, group_df in df.groupby(group_cols, dropna=False, sort=True):
        if not isinstance(keys, tuple):
            keys = (keys,)
        metrics = metric_block(group_df)
        metrics["level"] = level
        for col, value in zip(group_cols, keys):
            metrics[col] = value
        rows.append(metrics)
    return rows


def write_markdown_table(file_obj, df, columns, limit=None):
    if df.empty:
        file_obj.write("No rows.\n\n")
        return
    table_df = df.loc[:, [col for col in columns if col in df.columns]].copy()
    if limit:
        table_df = table_df.head(limit)
    for col in table_df.columns:
        if pd.api.types.is_float_dtype(table_df[col]):
            table_df[col] = table_df[col].map(format_number)
    header = "| " + " | ".join(table_df.columns) + " |"
    separator = "| " + " | ".join(["---"] * len(table_df.columns)) + " |"
    file_obj.write(header + "\n")
    file_obj.write(separator + "\n")
    for _, row in table_df.iterrows():
        values = ["" if pd.isna(value) else str(value) for value in row.tolist()]
        file_obj.write("| " + " | ".join(values) + " |\n")
    file_obj.write("\n\n")


def add_row_errors(df):
    result = df.copy()
    y_true = pd.to_numeric(result[TRUE_COL], errors="coerce")
    y_pred = pd.to_numeric(result[PRED_COL], errors="coerce")
    valid_mask = y_true.notna() & y_pred.notna() & np.isfinite(y_true) & np.isfinite(y_pred) & (y_true > 0)
    result["row_abs_error"] = np.nan
    result["row_ape"] = np.nan
    result["row_accuracy"] = np.nan
    result.loc[valid_mask, "row_abs_error"] = (y_true[valid_mask] - y_pred[valid_mask]).abs()
    result.loc[valid_mask, "row_ape"] = result.loc[valid_mask, "row_abs_error"] / y_true[valid_mask] * 100
    result.loc[valid_mask, "row_accuracy"] = 100 - result.loc[valid_mask, "row_ape"]
    return result


def main():
    parser = argparse.ArgumentParser(
        description="Compute row-level operator cost accuracy on chunk predictions only."
    )
    parser.add_argument(
        "--pred_path",
        required=True,
        help="Path to output/predictions/chunk_runtime_rows*.csv generated by analyze_chunk_runtime_scaling.py.",
    )
    parser.add_argument(
        "--output_suffix",
        default="",
        help="Optional output suffix. Defaults to the suffix inferred from pred_path.",
    )
    parser.add_argument(
        "--min_samples_for_worst",
        type=int,
        default=5,
        help="Minimum row count when listing worst grouped summaries.",
    )
    args = parser.parse_args()

    pred_path = Path(args.pred_path)
    if not pred_path.exists():
        raise FileNotFoundError(f"prediction csv not found: {pred_path}")

    ensure_output_dirs()
    suffix = args.output_suffix or infer_suffix(pred_path)

    df = pd.read_csv(pred_path, low_memory=False)
    if "source_kind" in df.columns:
        df = df[df["source_kind"] == "chunk"].copy()

    required_cols = {TRUE_COL, PRED_COL}
    missing_cols = sorted(required_cols - set(df.columns))
    if missing_cols:
        raise ValueError(f"missing required columns in {pred_path}: {missing_cols}")

    df = add_row_errors(df)

    summary_rows = []
    group_specs = [
        ("overall", []),
        ("by_ds_type", ["ds_type"]),
        ("by_chunk_size", ["chunk_size"]),
        ("by_ds_type_chunk_size", ["ds_type", "chunk_size"]),
        ("by_pipeline_category", ["ds_type", "pipeline_category"]),
        ("by_pipeline_category_chunk_size", ["ds_type", "pipeline_category", "chunk_size"]),
        ("by_pipeline_chunk_size", ["ds_type", "pipeline_base_name", "pipeline_category", "chunk_size"]),
        ("by_operator", ["ds_type", "operator_name"]),
        ("by_operator_chunk_size", ["ds_type", "operator_name", "chunk_size"]),
    ]
    for level, group_cols in group_specs:
        existing_cols = [col for col in group_cols if col in df.columns]
        if len(existing_cols) == len(group_cols):
            summary_rows.extend(summarize_group(df, level, group_cols))

    summary_df = pd.DataFrame(summary_rows)
    base_cols = [
        "level",
        "ds_type",
        "chunk_size",
        "pipeline_category",
        "pipeline_base_name",
        "operator_name",
        "row_count",
        "sample_count",
        "skipped_count",
        "rmse",
        "mae",
        "mape",
        "accuracy",
        "actual_time_sum",
        "pred_time_sum",
    ]
    summary_df = summary_df[[col for col in base_cols if col in summary_df.columns]]

    detail_cols = [
        "ds_type",
        "pipeline_name",
        "pipeline_base_name",
        "pipeline_category",
        "chunk_size",
        "chunk_part",
        "operator_index",
        "operator_name",
        TRUE_COL,
        PRED_COL,
        "row_abs_error",
        "row_ape",
        "row_accuracy",
        "chunk_profile_matched",
        "model_dir_used",
    ]
    detail_df = df[[col for col in detail_cols if col in df.columns]].copy()
    detail_df = detail_df.sort_values("row_ape", ascending=False, na_position="last")

    summary_path = SUMMARIES_DIR / f"chunk_operator_accuracy{suffix}.csv"
    detail_path = SUMMARIES_DIR / f"chunk_operator_accuracy_rows{suffix}.csv"
    report_path = REPORTS_DIR / f"chunk_operator_accuracy_report{suffix}.md"
    summary_df.to_csv(summary_path, index=False)
    detail_df.to_csv(detail_path, index=False)

    with open(report_path, "w", encoding="utf-8") as file_obj:
        file_obj.write("# Chunk Operator Cost Accuracy Report\n\n")
        file_obj.write(
            "This report evaluates each chunk operator row independently: "
            "`op_process_time` vs `op_process_time_pred`. It does not sum chunks "
            "and does not compare against the full-scale run.\n\n"
        )
        file_obj.write("Metric definition is consistent with the operator-level cost reports: ")
        file_obj.write("`MAPE = mean(abs(y_true - y_pred) / y_true) * 100`, ")
        file_obj.write("`accuracy = 100 - MAPE`; rows with `op_process_time <= 0` are skipped.\n\n")

        overall = summary_df[summary_df["level"] == "overall"]
        file_obj.write("## Overall\n\n")
        write_markdown_table(
            file_obj,
            overall,
            ["sample_count", "rmse", "mae", "mape", "accuracy", "actual_time_sum", "pred_time_sum"],
        )

        file_obj.write("## By Modality\n\n")
        write_markdown_table(
            file_obj,
            summary_df[summary_df["level"] == "by_ds_type"],
            ["ds_type", "sample_count", "rmse", "mae", "mape", "accuracy"],
        )

        file_obj.write("## By Modality And Chunk Size\n\n")
        write_markdown_table(
            file_obj,
            summary_df[summary_df["level"] == "by_ds_type_chunk_size"],
            ["ds_type", "chunk_size", "sample_count", "rmse", "mae", "mape", "accuracy"],
        )

        file_obj.write("## By Pipeline Category And Chunk Size\n\n")
        write_markdown_table(
            file_obj,
            summary_df[summary_df["level"] == "by_pipeline_category_chunk_size"],
            ["ds_type", "pipeline_category", "chunk_size", "sample_count", "rmse", "mae", "mape", "accuracy"],
        )

        worst_groups = summary_df[
            (summary_df["level"].isin(["by_operator", "by_pipeline_chunk_size"]))
            & (summary_df["sample_count"] >= args.min_samples_for_worst)
        ].sort_values("mape", ascending=False)
        file_obj.write("## Worst Groups\n\n")
        write_markdown_table(
            file_obj,
            worst_groups,
            [
                "level",
                "ds_type",
                "pipeline_base_name",
                "pipeline_category",
                "operator_name",
                "chunk_size",
                "sample_count",
                "mape",
                "accuracy",
            ],
            limit=30,
        )

        file_obj.write("## Worst Rows\n\n")
        write_markdown_table(
            file_obj,
            detail_df,
            [
                "ds_type",
                "pipeline_name",
                "chunk_size",
                "chunk_part",
                "operator_index",
                "operator_name",
                TRUE_COL,
                PRED_COL,
                "row_ape",
                "row_accuracy",
                "chunk_profile_matched",
            ],
            limit=30,
        )

    print(f"saved summary csv: {summary_path}")
    print(f"saved row detail csv: {detail_path}")
    print(f"saved report: {report_path}")

    overall = summary_df[summary_df["level"] == "overall"].iloc[0]
    print("\nchunk row-level operator accuracy")
    print(f"sample_count: {int(overall['sample_count'])}")
    print(f"RMSE: {format_number(overall['rmse'])}")
    print(f"MAE: {format_number(overall['mae'])}")
    print(f"MAPE: {format_number(overall['mape'])}%")
    print(f"accuracy: {format_number(overall['accuracy'])}%")


if __name__ == "__main__":
    main()
